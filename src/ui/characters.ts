export function charByKind(kind: string): Character {
  return (
    CHARACTERS.find((c) => c.kind === kind) ??
    PREMIUM_CAST.find((c) => c.kind === kind) ??
    CHARACTERS[0]
  );
}

export function pickLine(kind: string, ev: EmoteEvent): string {
  const lines = charByKind(kind).lines[ev];
  return lines[Math.floor(Math.random() * lines.length)];
}

export function pickRankLine(kind: string, rank: RankKey): string {
  const lines = charByKind(kind).rankLines[rank];
  return lines[Math.floor(Math.random() * lines.length)];
}

/** 카테고리(+구성 카드)에서 족보 대사 키 도출. 투페어 이하 = null */
export function rankKeyOf(cat: number, best5: { rank: number }[]): RankKey | null {
  if (cat === 8) return 'stflush';
  if (cat === 7) return 'quads';
  if (cat === 6) return 'fullhouse';
  if (cat === 5) return 'flush';
  if (cat === 4) {
    const rs = best5.map((c) => c.rank).sort((a, b) => b - a);
    if (rs[0] === 14 && rs[1] === 13) return 'mountain';
    if (rs[0] === 14 && rs[1] === 5) return 'wheel';
    return 'straight';
  }
  if (cat === 3) return 'trips';
  return null;
}
